#include "maths.h"

int	abs_d(int a)
{
	return (a * ((a >= 0) * 2 - 1));
}

float	abs_ff(float a)
{
	return (a * ((a >= 0) * 2 - 1));
}

double	abs_fd(double a)
{
	return (a * ((a >= 0) * 2 - 1));
}
